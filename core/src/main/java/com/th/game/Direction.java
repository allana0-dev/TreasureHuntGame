package com.th.game;

/**
 * Represents the four cardinal directions used for character movement.
 */
public enum Direction {

    /** Movement toward the positive Y axis (upwards). */
    UP,

    /** Movement toward the negative Y axis (downwards). */
    DOWN,

    /** Movement toward the negative X axis (leftwards). */
    LEFT,

    /** Movement toward the positive X axis (rightwards). */
    RIGHT
}
